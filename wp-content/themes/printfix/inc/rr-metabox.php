			array(
				
				'label'           => esc_html__('Select Footer Style', 'printfix'),
				'id'              => "{$prefix}_footer_style",
				'type'            => 'select',
				'options'         => array(
					'footer_1' => esc_html__( 'Footer 1', 'printfix' ),
					'footer_2' => esc_html__( 'Footer 2', 'printfix' ),
				),
				'placeholder'     => esc_html__( 'Select a footer', 'printfix' ),
				'conditional' => array(
					"{$prefix}_footer_tabs", "==", "custom"
				),
				'default' => 'footer_1',
				'parent' => "{$prefix}_footer_tabs"
			),

		),
	);

    $meta_boxes[] = array(
		'metabox_id'       => $prefix . '_post_gallery_meta',
		'title'    => esc_html__( 'TP Gallery Post', 'printfix' ),
